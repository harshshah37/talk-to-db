DROP TABLE IF EXISTS Stock;
CREATE TABLE Stock (
    StockCode VARCHAR(15) PRIMARY KEY NOT NULL UNIQUE,
    Description VARCHAR(40) NOT NULL,
    UnitPrice NUMERIC(5, 2) NOT NULL
);

DROP TABLE IF EXISTS Invoice;
CREATE TABLE Invoice (
    InvoiceNo VARCHAR(10) PRIMARY KEY NOT NULL,
    StockCode VARCHAR(15) NOT NULL,
    InvoiceDate TIMESTAMP NOT NULL,
    Quantity INT NOT NULL,
	CONSTRAINT fk_stock FOREIGN KEY (StockCode) REFERENCES Stock(StockCode)
);

DROP TABLE IF EXISTS Customers;
CREATE TABLE Customers (
    CustomerID INT PRIMARY KEY NOT NULL UNIQUE,
    InvoiceNo VARCHAR(10) NOT NULL UNIQUE,
    Country VARCHAR(25),
    CONSTRAINT fk_invoice FOREIGN KEY (InvoiceNo) REFERENCES Invoice(InvoiceNo)
);

