-- Get the Total Sales for Each Product
SELECT s.StockCode, s.Description, 
       SUM(i.Quantity * s.UnitPrice) AS TotalSales
FROM Stock s
JOIN Invoice i ON s.StockCode = i.StockCode
GROUP BY s.StockCode, s.Description
ORDER BY TotalSales DESC
LIMIT 10;

-- Find the Most Popular Products (Based on Quantity Sold)
SELECT s.StockCode, s.Description, 
       SUM(i.Quantity) AS TotalQuantitySold
FROM Stock s
JOIN Invoice i ON s.StockCode = i.StockCode
GROUP BY s.StockCode, s.Description
ORDER BY TotalQuantitySold DESC
LIMIT 10;

-- Get the Customers Purchase History
SELECT c.CustomerID, c.Country, i.InvoiceNo, i.StockCode, s.Description, i.Quantity, i.InvoiceDate
FROM Customers c
JOIN Invoice i ON c.InvoiceNo = i.InvoiceNo
JOIN Stock s ON i.StockCode = s.StockCode
WHERE c.CustomerID = 16806  -- Replace with a specific CustomerID
ORDER BY i.InvoiceDate DESC;

-- Find Customers Who Have Purchased a Specific Product
SELECT DISTINCT c.CustomerID, c.Country, c.InvoiceNo
FROM Customers c
JOIN Invoice i ON c.InvoiceNo = i.InvoiceNo
WHERE i.StockCode = '22992'  -- Replace with the desired StockCode
ORDER BY c.CustomerID;

-- Find the Products That Have Never Been Sold
SELECT s.StockCode, s.Description
FROM Stock s
LEFT JOIN Invoice i ON s.StockCode = i.StockCode
WHERE i.InvoiceNo IS NULL;


